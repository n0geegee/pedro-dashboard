#!/usr/bin/env python3
"""Regenerate mock state JSON files for Pedro Dashboard (Etap 1).

Idempotent. Writes all 8 widget state files to app/state/.

Usage:
    python3 scripts/write-mock-state.py            # overwrite with current time
    python3 scripts/write-mock-state.py --check    # exit 1 if any file is missing or stale
    python3 scripts/write-mock-state.py --out DIR  # write to a different state dir

Atomic write: temp file in the same directory + os.replace(). Never leaves
half-written JSON in app/state/.

All payloads are mock-only and contain no secrets, raw logs, or private
Discord content. Privacy mode default = "private" (MVP) and can be overridden
by DASHBOARD_PRIVACY_MODE env var.
"""
from __future__ import annotations

import argparse
