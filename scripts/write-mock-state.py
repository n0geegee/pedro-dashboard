from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent.parent
APP_DIR = PROJECT_ROOT / "app"
DEFAULT_STATE_DIR = APP_DIR / "state"

PRIVACY_MODE = os.environ.get("DASHBOARD_PRIVACY_MODE", "private")


def _now_iso() -> str:
    return datetime.now(timezone.utc).isoformat(timespec="seconds")


def _is_stale(updated_at: object, ttl_seconds: object) -> bool:
    try:
        ttl = float(ttl_seconds)
    except (TypeError, ValueError):
        return False
    if ttl <= 0:
        return False
    if not isinstance(updated_at, str) or not updated_at:
        return True
    try:
        stamp = datetime.fromisoformat(updated_at.replace("Z", "+00:00"))
    except ValueError:
        return True
    if stamp.tzinfo is None:
        stamp = stamp.replace(tzinfo=timezone.utc)
    return (datetime.now(timezone.utc) - stamp.astimezone(timezone.utc)).total_seconds() > ttl


def _atomic_write(path: Path, payload: dict) -> None:
    """Write JSON atomically: temp file in same dir + flush + replace."""
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_suffix(path.suffix + ".tmp")
    with tmp.open("w", encoding="utf-8") as f:
        json.dump(payload, f, ensure_ascii=False, indent=2)
        f.write("\n")
        f.flush()
        try:
            os.fsync(f.fileno())
        except OSError:
            pass
    os.replace(tmp, path)


def _envelope(widget: str, status: str, ttl: int, data: dict, error: dict | None = None) -> dict:
    return {
        "status": status,
        "updated_at": _now_iso(),
        "ttl_seconds": ttl,
        "privacy_mode": PRIVACY_MODE,
        "data": data,
        "error": error,
    }


