# ---- driver ----------------------------------------------------------------


WIDGETS = {
    "dashboard.json": widget_dashboard,
    "system.json": widget_system,
    "hermes.json": widget_hermes,
    "openviking.json": widget_openviking,
    "current_focus.json": widget_current_focus,
    "alerts.json": widget_alerts,
    "decisions.json": widget_decisions,
    "voice_console.json": lambda: widget_voice_console("idle"),
    # Live-owned widgets are written by refresh-*.py connectors below, not by
    # mock baseline. Keeping them out of the baseline prevents visible races
    # where the 20s refresher briefly shows stale mock values before live probes
    # overwrite them again.
    # - weather.json -> refresh-weather-status.py
    # - route.json -> refresh-route-status.py
    # - calendar.json -> refresh-kamila-calendar.py
    # - volleyball.json -> refresh-match-calendar.py
    # - media.json slideshow -> refresh-photos-slideshow.py
    "ll_tbd.json": widget_ll_tbd,
}


def write_all(state_dir: Path) -> list[Path]:
    written: list[Path] = []
    for name, fn in WIDGETS.items():
        path = state_dir / name
        _atomic_write(path, fn())
        written.append(path)
    return written


def check_all(state_dir: Path) -> int:
    """Return count of missing, invalid, or stale files. Used by --check."""
    problems: list[str] = []
    for name in WIDGETS:
        path = state_dir / name
        if not path.exists():
            problems.append(f"missing:{name}")
            continue
        try:
            payload = json.loads(path.read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError) as exc:
            problems.append(f"invalid:{name}:{exc}")
            continue
        if not isinstance(payload, dict):
            problems.append(f"invalid:{name}:payload_not_object")
            continue
        if _is_stale(payload.get("updated_at"), payload.get("ttl_seconds")):
            problems.append(f"stale:{name}")
    if problems:
        print("problems: " + ", ".join(problems), file=sys.stderr)
    return len(problems)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("--check", action="store_true", help="verify state files exist; exit 1 if any missing")
    parser.add_argument("--out", default=str(DEFAULT_STATE_DIR), help="output state directory")
    args = parser.parse_args(argv)

    state_dir = Path(args.out).resolve()
    if args.check:
        missing = check_all(state_dir)
        if missing:
            print(f"FAIL: {missing} state file(s) missing in {state_dir}", file=sys.stderr)
            return 1
        print(f"OK: {len(WIDGETS)} state files present, valid, and fresh in {state_dir}")
        return 0

    state_dir.mkdir(parents=True, exist_ok=True)
    written = write_all(state_dir)
    print(f"wrote {len(written)} mock state files to {state_dir}")
    for p in written:
        print(f"  - {p.name}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
